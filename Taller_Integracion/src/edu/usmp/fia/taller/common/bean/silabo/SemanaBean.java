package edu.usmp.fia.taller.common.bean.silabo;

public class SemanaBean {
	
	String idSemana;
	int numSesion;
	int numSemana;
	String titulo;
	String idSilabo;
	
	public String getIdSemana() {
		return idSemana;
	}
	public void setIdSemana(String idSemana) {
		this.idSemana = idSemana;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getIdSilabo() {
		return idSilabo;
	}
	public void setIdSilabo(String idSilabo) {
		this.idSilabo = idSilabo;
	}
	public int getNumSesion() {
		return numSesion;
	}
	public void setNumSesion(int numSesion) {
		this.numSesion = numSesion;
	}
	public int getNumSemana() {
		return numSemana;
	}
	public void setNumSemana(int numSemana) {
		this.numSemana = numSemana;
	}
	
}
