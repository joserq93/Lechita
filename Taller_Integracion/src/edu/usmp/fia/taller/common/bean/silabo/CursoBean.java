package edu.usmp.fia.taller.common.bean.silabo;

public class CursoBean {
	
	String idCurso;
	String nombreCurso;
	String estadoCruso;
	String tipoCurso;
	String sumillaCurso;
	
	public String getSumillaCurso() {
		return sumillaCurso;
	}
	public void setSumillaCurso(String sumillaCurso) {
		this.sumillaCurso = sumillaCurso;
	}
	public String getIdCurso() {
		return idCurso;
	}
	public void setIdCurso(String idCurso) {
		this.idCurso = idCurso;
	}
	public String getNombreCurso() {
		return nombreCurso;
	}
	public void setNombreCurso(String nombreCurso) {
		this.nombreCurso = nombreCurso;
	}
	public String getEstadoCruso() {
		return estadoCruso;
	}
	public void setEstadoCruso(String estadoCruso) {
		this.estadoCruso = estadoCruso;
	}
	public String getTipoCurso() {
		return tipoCurso;
	}
	public void setTipoCurso(String tipoCurso) {
		this.tipoCurso = tipoCurso;
	}
	
}
