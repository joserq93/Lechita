package edu.usmp.fia.taller.common.bean.silabo;

public class BibliografiaBean {
	
	String idBibliografia;
	String id_silabo;
	String reseña;
	public String getIdBibliografia() {
		return idBibliografia;
	}
	public void setIdBibliografia(String idBibliografia) {
		this.idBibliografia = idBibliografia;
	}
	public String getId_silabo() {
		return id_silabo;
	}
	public void setId_silabo(String id_silabo) {
		this.id_silabo = id_silabo;
	}
	public String getReseña() {
		return reseña;
	}
	public void setReseña(String reseña) {
		this.reseña = reseña;
	}
	
	
}
